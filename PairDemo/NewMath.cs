using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PairDemo
{
    internal class NewMath
    {
        public static double Adder(double x, double y)
        {
            return x + y;
        }
        public static double Subber(double x, double y)
        {
            return x - y;
        }
        public static double Multipler(double x, double y)
        {
            return x * y;
        }
        public static double Divider(double x, double y)
        {
            return x / y;
        }
    }
}
